public class newpattern3 {

	public static void main(String[] args) {
		
		int row = 10 ; 
		
		for(int i = 0 ; i<row ; i++) {
			
			for(int j=i ; j<row ; j++) {
				
				
				System.out.print("* ");
				
			}
			System.out.println();
		}

	}

}
